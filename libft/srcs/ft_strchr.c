#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char c2;

	c2 = c;
	while (*s != c2 && *s != '\0')
		s++;
	if (*s == c2)
		return ((void *)s);
	else
		return (NULL);
}
