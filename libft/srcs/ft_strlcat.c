#include "libft.h"

size_t		ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t	len;
	int		i;

	len = ft_strlen(dst);
	i = 0;
	if (size <= len)
		return (size + ft_strlen(src));
	while ((len + i) < (size - 1) && src[i] != '\0')
	{
		dst[len + i] = src[i];
		i++;
	}
	dst[len + i] = '\0';
	return (len + ft_strlen(src));
}
